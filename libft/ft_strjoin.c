#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	size_t	len1;
	size_t	len2;
	char	*str;

	if (!s1 || !s2)
		return (NULL);
	len1 = ft_strlen(s1);
	len2 = ft_strlen(s2);
	str = malloc (len1 + len2 + 1);
	if (!str)
		return (NULL);
	str[0] = '\0';
	ft_strlcat(str, s1, len1 + len2 + 1);
	ft_strlcat(str, s2, len1 + len2 + 1);
	return (str);
}
